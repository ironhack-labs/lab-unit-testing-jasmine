function sumArrayNumbers(num1, num2, num3) {
    if (sumArrayNumbers !== typeof Array) {
        return undefined
    }
    else if (sumArrayNumbers.length == []) {
        return 0
    }
    else if (sumArrayNumbers.length === typeof Number) {
        return num1 + num2 + num3
    }
}


